#include "3-calc.h"

/**
 * op_add - add a and b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: add a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract a and b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: substract a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a and b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: multiplies a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a and b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: divide a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod a and b
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: mod a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
